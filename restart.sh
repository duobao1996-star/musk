#!/bin/bash

################################################################################
# 项目重启脚本
# 功能：停止所有服务后重新启动
################################################################################

# 颜色定义
GREEN='\033[0;32m'
CYAN='\033[0;36m'
NC='\033[0m'

# 获取脚本所在目录
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

print_title() {
    echo -e "${CYAN}"
    echo "════════════════════════════════════════════════════════════════"
    echo "  $1"
    echo "════════════════════════════════════════════════════════════════"
    echo -e "${NC}"
}

clear

print_title "Musk管理系统 - 重启脚本"

echo -e "${GREEN}正在重启服务...${NC}"
echo ""

# 1. 停止服务
bash stop.sh

echo ""
sleep 2

# 2. 启动服务
bash start.sh

